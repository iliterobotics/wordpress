<h1>ILITE</h1>
We are ILITE, and the very breakdown of our name tells you who we are and what we are doing: Inspiring Leaders in Technology and Engineering. To accomplish this end goal, we have focused our effors on five main pillars.

<h1>Community Serivce</h1>
The foundation of our team is giving back to the community around us. By doing so, we are helping show the positive impact that FIRST and ILITE can have on one’s life. By helping others, we realize how fortunate we are and how we can truly utilize the talents we have developed to benefit the common good.

<h1>Learning</h1>
When we are not trying to further the community, we spend endless hours trying to improve ourselves. By studying and inspiring interest in a field that is constantly evolving, there is never a time when one knows everything they could know on this topic.

<h1>Growth</h1>
We are constantly striving to grow as individuals; be it by learning how to interact in a team setting, how to take or give advice, or something new about ourselves as individuals. This is because we believe that through the growth of each member as an individual we are helping to cater to the growth of a further desire to pursue knowledge in technology and engineering, as well as it helps the growth of the team as a whole.

<h1>Opportunity</h1>
Through this program, we have opened the door to opportunities that many would not be able to experience without ILITE. This opportunity is due to ILITE fostering the growth of skills that allow alumni to go out and be successful in any avenue of their choosing.

<h1>Teaching</h1>
We have all been so deeply changed by our participation in this program that we wish to share what we have learned with future generations. As such, we partake in many activities that allow younger children a first-hand experience with technology and engineering.
